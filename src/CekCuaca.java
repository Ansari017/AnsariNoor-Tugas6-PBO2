    private void dataCuaca() {
        String filePath = "src/data_cuaca.csv";
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);

        try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(",");

                if (data.length == 3) {
                    model.addRow(data);
                }
            }
            JOptionPane.showMessageDialog(this, "Data cuaca berhasil dimuat ke dalam tabel.");
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal memuat data cuaca dari file.");
        }
    }
    
    private boolean kotaCbox(String city) {
    for (int i = 0; i < jComboBox1.getItemCount(); i++) {
        if (jComboBox1.getItemAt(i).equals(city)) {
            return true;
        }
    }
    return false;
    }

    private String API_KEY = "f2c4e46e7aba3744d56524fd1896414d";

    private String fetchWeatherData(String city) {
        StringBuilder result = new StringBuilder();
        try {
            String urlString = "http://api.openweathermap.org/data/2.5/weather?q=" + city + "&appid=" + API_KEY + "&units=metric";
            URL url = new URL(urlString);
            HttpURLConnection conn = (HttpURLConnection) url.openConnection();
            conn.setRequestMethod("GET");

            BufferedReader rd = new BufferedReader(new InputStreamReader(conn.getInputStream()));
            String line;
            while ((line = rd.readLine()) != null) {
                result.append(line);
            }
            rd.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        return result.toString();
    }
    
